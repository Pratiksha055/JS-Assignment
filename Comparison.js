let x = "10";
let y = 10;

// == vs ===
console.log("x == y:", x == y);   
console.log("x === y:", x === y); 

// != vs !==
console.log("x != y:", x != y);   
console.log("x !== y:", x !== y); 

// Relational Operators
console.log("x > y:", x > y);
console.log("x < y:", x < y);
console.log("x >= y:", x >= y);
console.log("x <= y:", x <= y);

// Explanation
// == checks only value and allows type conversion.
// === checks both value and type (strict equality).
